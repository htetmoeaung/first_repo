package module1.chapter2control_flow;

public class Lesson5ForrLoop {
    public static void main(String[] args) {
        for (int a =3; a<=15; a+=3){
            System.out.print(a+" ");
        }

        System.out.println(" ");

        for (int b=9; b>0; b-=2){
            System.out.print(b+" ");
        }
    }
}
